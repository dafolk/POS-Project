    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        Cashier cashier = new Cashier();
        
        if(isPerfect() && isPasswordConfirm()){
            cashier.setName(this.txtName.getText());
            cashier.setUsername(this.txtUsername.getText());
            cashier.setPassword(PasswordUtil.hashPassword(this.pwdPassword.getText()));
            
            cashierService.insertCashier(cashier);

            JOptionPane.showMessageDialog(null, "Account successfully created!", "Sign Up Done!", INFORMATION_MESSAGE);
            
            this.txtName.setText("");
            this.txtUsername.setText("");
            this.pwdPassword.setText("");
            this.pwdPasswordConfirm.setText("");
        }
        else{
            if(!isPerfect()){
                JOptionPane.showMessageDialog(null, "All fields must be filled!", "Fields cannot be null!", WARNING_MESSAGE);
            }
            
            else{
                JOptionPane.showMessageDialog(null, "Passwords must be matched!", "Passwords Do Not Match!", WARNING_MESSAGE);
                this.pwdPassword.setText("");
                this.pwdPasswordConfirm.setText("");
            }
        }
    }//GEN-LAST:event_btnSignUpActionPerformed
