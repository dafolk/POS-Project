    private boolean isPerfect(){
        if(this.txtName.getText().isEmpty()){
            return false;
        }
        else if(this.txtUsername.getText().isEmpty()){
            return false;
        }
        else if(this.pwdPassword.getText().isEmpty()){
            return false;
        }
        else if(this.pwdPasswordConfirm.getText().isEmpty()){
            return false;
        }
        else{
           return true; 
        }
    }
    
    private boolean isPasswordConfirm(){
        if(this.pwdPassword.getText().equals(this.pwdPasswordConfirm.getText())){
            return true;
        }
        else{
            return false;
        }
    }
    
